function micro(id, title, yearRange, ageRange, category, condition, conditionalText, fallbackText, effects = H, extra = {}) {
  return {
    id: `daily_micro_${id}`,
    title,
    category,
    yearRange,
    ageRange,
    lifetimeProbability: 0.24,
    baseWeight: 8 + (id.length % 6),
    maxOccurrences: 1,
    text: [
      { conditions: condition, text: conditionalText },
      { text: fallbackText },
    ],
    effects,
    tags: ["ordinary_microtexture"],
    ...extra,
  };
}

