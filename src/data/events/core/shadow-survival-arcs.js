//
// These chains do not ask the player to choose and do not guarantee repentance,
// punishment or redemption. Scarcity can explain a harmful act without making
// it harmless. Hidden `shadow` state lets later years remember what the public
// status bars cannot: complicity, guilt, hardening and self-deception.

