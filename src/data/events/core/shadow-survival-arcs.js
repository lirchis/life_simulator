function survivalDomain(id) {
  return `shadow_survival_${SURVIVAL_DOMAINS.find(([, ids]) => ids.includes(id))?.[0] ?? "other"}`;
}

function shadowEvent({
  id,
  title,
  category,
  yearRange,
  ageRange,
  text,
  effects,
  conditions,
  currentRegions,
  requiresEvents,
  narrativeTier = "turning_point",
  lifetimeProbability = 0.12,
  baseWeight = 22,
  close = false,
}) {
  return {
    id: `shadow_survival_${id}`,
    title,
    category,
    yearRange,
    ageRange,
    maxOccurrences: 1,
    baseWeight: narrativeTier === "consequence" ? Math.max(baseWeight, 38) : baseWeight,
    ...(narrativeTier === "turning_point" ? { lifetimeProbability } : {}),
    ...(conditions ? { conditions } : {}),
    ...(currentRegions ? { currentRegions } : {}),
    ...(requiresEvents ? { requiresEvents: requiresEvents.map((item) => `shadow_survival_${item}`) } : {}),
    text,
    effects,
    narrativeTier,
    narrativeDomain: survivalDomain(id),
    narrativeThread: close ? { close: true } : { expiresAfterYears: 24 },
  };
}

