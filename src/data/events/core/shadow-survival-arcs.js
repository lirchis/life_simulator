const after = (eventId, eventIdToUnlock, maxYears) => ({
  all: [{
    eventOccurredBetween: {
      eventId: `shadow_survival_${eventId}`,
      minYears: MIN_DELAYS[eventIdToUnlock],
      maxYears,
    },
  }],
});
const schedule = (eventId, minDelay, maxDelay) => [
  {
    scheduleEvent: {
      eventId: `shadow_survival_${eventId}`,
      delayYears: [minDelay, maxDelay],
      weightMultiplier: 4,
    },
  },
];

