// A fictional future history for 2036—2120.
//
// These events are literary hypotheses, not factual forecasts. They deliberately
// make institutions, conflicts and collective memory visible, so the future is
// not reduced to a catalogue of nicer gadgets. A life only sees the parts that
// match its age, place, class and prior events; no player choice is introduced.

const add = (path, value) => ({ path, add: value });
const C = (path, operator, value) => ({ path, [operator]: value });

function futureHistory({
  id,
  title,
  category,
  yearRange,
  ageRange = [8, 99],
  text,
  effects = [],
  conditions,
  currentRegions,
  requiresEvents,
  narrativeTier = "historical_pressure",
  narrativeDomain = "history",
  lifetimeProbability,
  baseWeight = 44,
  close = false,
}) {
  const inferredProbability = narrativeTier === "historical_pressure" ? 0.72 : undefined;
  const probability = lifetimeProbability === null ? undefined : lifetimeProbability ?? inferredProbability;
  return {
    id: `spec_${id}`,
    title,
    category,
    yearRange,
    ageRange,
    maxOccurrences: 1,
    baseWeight,
    ...(probability === undefined ? {} : { lifetimeProbability: probability }),
    ...(conditions ? { conditions } : {}),
    ...(currentRegions ? { currentRegions } : {}),
    ...(requiresEvents ? { requiresEvents: requiresEvents.map((item) => `spec_${item}`) } : {}),
    text,
    effects,
    narrativeTier,
    narrativeDomain,
    ...(close ? { narrativeThread: { close: true } } : {}),
  };
}

const H = (config) => futureHistory(config);
const consequence = (config) => futureHistory({
  ...config,
  narrativeTier: "consequence",
  lifetimeProbability: null,
});

