export function composeQuietYearText(state, rng) {
  const year = state.meta.currentYear;
  if (state.meta.age <= 3) return composeEarlyChildhoodText(state, rng);
  const scenes = getEraScenes(year, isRuralSetting(state), state.location.currentProvince);
  const details = getLifeDetails(state);
  const reflections = getQuietYearClosings(state);
  const frames = [
    (scene, detail, reflection) => `${year}年没有一件事大到足以单独留下名字。${scene}${detail}${reflection}`,
    (scene, detail, reflection) => `日历翻到${year}年。${scene}${detail}${reflection}`,
    (scene, detail, reflection) => `后来回想${year}年，你先记起的不是大事。${scene}${detail}${reflection}`,
    (scene, detail, reflection) => `${year}年从许多小事中间走了过去。${scene}${detail}${reflection}`,
    (scene, detail, reflection) => `${year}年，屋里的物件大致还在原处，住在屋里的人却各自添了一岁。${scene}${detail}${reflection}`,
    (scene, detail, reflection) => `${year}年大体平常。${scene}${detail}${reflection}`,
  ];

  const used = new Set(state.history
    .filter((item) => item.eventId === "life_quiet_year")
    .map((item) => normalizeForReuse(item.text)));
  for (let attempt = 0; attempt < 24; attempt += 1) {
    const text = choose(frames, rng)(choose(scenes, rng), choose(details, rng), choose(reflections, rng));
    if (!used.has(normalizeForReuse(text))) return text;
  }
  return `${year}年后来只剩下零散细节。${choose(scenes, rng)}${choose(details, rng)}${choose(reflections, rng)}`;
}

